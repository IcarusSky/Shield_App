package com.crec.shield.entity.project.risk;

public class RiskHeadCurrentRisks {
    public String riskId;
    public String current_risk_detail;
    public String current_risk_cross;
    public String current_circle_num;
    public String current_expect_date;

    public String getRiskId() {
        return riskId;
    }

    public void setRiskId(String riskId) {
        this.riskId = riskId;
    }

    public String getCurrent_risk_detail() {
        return current_risk_detail;
    }

    public void setCurrent_risk_detail(String current_risk_detail) {
        this.current_risk_detail = current_risk_detail;
    }

    public String getCurrent_risk_cross() {
        return current_risk_cross;
    }

    public void setCurrent_risk_cross(String current_risk_cross) {
        this.current_risk_cross = current_risk_cross;
    }

    public String getCurrent_circle_num() {
        return current_circle_num;
    }

    public void setCurrent_circle_num(String current_circle_num) {
        this.current_circle_num = current_circle_num;
    }

    public String getCurrent_expect_date() {
        return current_expect_date;
    }

    public void setCurrent_expect_date(String current_expect_date) {
        this.current_expect_date = current_expect_date;
    }
}
