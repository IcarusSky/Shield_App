package com.crec.shield.entity.overview.overview;

public class ApproachingPromptEntity {

    private Integer type;
    private String lineName;
    private String totalRings;
    private String startDate;
    private String sectionStart;
    private String sectionEnd;
    private String remainDays;
    private String remainRings;
    private String endDate;

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getLineName() {
        return lineName;
    }

    public void setLineName(String lineName) {
        this.lineName = lineName;
    }

    public String getTotalRings() {
        return totalRings;
    }

    public void setTotalRings(String totalRings) {
        this.totalRings = totalRings;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getSectionStart() {
        return sectionStart;
    }

    public void setSectionStart(String sectionStart) {
        this.sectionStart = sectionStart;
    }

    public String getSectionEnd() {
        return sectionEnd;
    }

    public void setSectionEnd(String sectionEnd) {
        this.sectionEnd = sectionEnd;
    }

    public String getRemainDays() {
        return remainDays;
    }

    public void setRemainDays(String remainDays) {
        this.remainDays = remainDays;
    }

    public String getRemainRings() {
        return remainRings;
    }

    public void setRemainRings(String remainRings) {
        this.remainRings = remainRings;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }
}
