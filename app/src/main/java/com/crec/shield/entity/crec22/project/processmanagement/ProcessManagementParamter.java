package com.crec.shield.entity.crec22.project.processmanagement;

public class ProcessManagementParamter {
    String projectId;
    String projectName;
    String finishNumber;
    String dayNumber;
    String nightNumber;

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public String getFinishNumber() {
        return finishNumber;
    }

    public void setFinishNumber(String finishNumber) {
        this.finishNumber = finishNumber;
    }

    public String getDayNumber() {
        return dayNumber;
    }

    public void setDayNumber(String dayNumber) {
        this.dayNumber = dayNumber;
    }

    public String getNightNumber() {
        return nightNumber;
    }

    public void setNightNumber(String nightNumber) {
        this.nightNumber = nightNumber;
    }
}
